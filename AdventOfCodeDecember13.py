    sum = 0
    for part in string.split("\n\n"):
        rows = part.split("\n")
        matrix = np.array([list(row) for row in rows])
        vertical = calculatePosition(matrix)
        horizontal = calculatePosition(np.rot90(matrix, k=-1))
