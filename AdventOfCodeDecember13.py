import numpy as np

def calculatePosition(matrix):
    for i in range(1, len(matrix)):
            print("i: " + str(i))
            highM = matrix[:i]
            lowM = matrix[i:2*i]
            rangeHigh = len(highM)
            rangeLow = len(lowM)
            high = np.array(highM[abs(rangeHigh-rangeLow):])
            low = np.array(lowM)
            print(high)
            print(low)  
            if np.array_equal(high, np.flipud(low)):
                return i
    return 0

